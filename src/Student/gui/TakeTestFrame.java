    public void loadQuestions(){
        try{
            ArrayList<Questions> quesList = QuestionDAO.getQuestionsByExamId(examobj.getExamID());
            for(Questions obj:quesList){
                qstore.addQuestion(obj);
            }
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, "error while connecting to db");
        }
    }
    public String getUserAnswer(){
        if(jrbOption1.isSelected())
            return "Answer 1";
        else if(jrbOption2.isSelected())
            return "Answer 2";
        else if(jrbOption3.isSelected())
            return "Answer 3";
        else if(jrbOption4.isSelected())
            return "Answer 4";
        else 
            return null;
    }
    
    public void showQuestions(){
        Questions questions = qstore.getQuestion(pos);
        buttonGroup1.clearSelection();
        txtQuestion.setText(questions.getQuestion());
        jrbOption1.setText(questions.getAnswer1());
        jrbOption2.setText(questions.getAnswer2());
        jrbOption3.setText(questions.getAnswer3());
        jrbOption4.setText(questions.getAnswer4());
        
        Answer answer = astore.getAnswerByQno(qno);
        if(answer==null)
            return;
        
        String chosenAnswer = answer.getChosenAnswer();
        switch(chosenAnswer){
            case "Answer 1" :
                jrbOption1.setSelected(true);
                break;
            case "Answer 2" :
                jrbOption2.setSelected(true);
                break;
            case "Answer 3" :
                jrbOption3.setSelected(true);
                break;
            case "Answer 4" :
                jrbOption4.setSelected(true);
                break;
        }
    }


