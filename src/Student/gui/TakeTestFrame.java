    private void btnDoneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDoneActionPerformed
        String chosenAnswer = getUserAnswer();
        if(chosenAnswer!=null){
        Questions ques = qstore.getQuestion(pos);
        String correctAns = ques.getCorrectAnswer();
        Answer newAnswer  = new Answer(examobj.getExamID(), examobj.getLanguage(), qno, chosenAnswer, correctAns);
        Answer answer = astore.getAnswerByQno(qno);
        if(answer == null)
            astore.addAnswer(newAnswer);
        else{
            if(newAnswer.getChosenAnswer().equals(answer.getChosenAnswer())== false)
                {  //int apos = astore.removeAnswer(answer);
                    //astore.setAnswer(apos, newAnswer);
                    astore.removeAnswerAt(pos);
                    astore.setAnswer(pos, newAnswer);
                }
            }}
        int right = 0;
        int wrong = 0;
        for(Questions question:qstore.getAllQuestions())
        {
            int qno = question.getQno();
            Answer answer = astore.getAnswerByQno(qno);
            if(answer == null)
                continue;
            else{
                String chosenAnswer1 = answer.getChosenAnswer();
                String correctAnswer = answer.getCorrectAnswer();
                if(chosenAnswer1.equals(correctAnswer))
                    ++right;
                else
                    ++wrong;
                }
        }
        String str = "Total Qusetions : "+qstore.getCount();
        str+= "\nRight answer : "+right;
        str+= "\nWrong answer : "+wrong;
        str+="\nUnattempted : "+(qstore.getCount()-(right+wrong));
        JOptionPane.showMessageDialog(null, str, "Your Result!", JOptionPane.INFORMATION_MESSAGE);
        try{
            Performance perf = new Performance(UserProfile.getUsername(), examobj.getExamID(), right, wrong, qstore.getCount()-(right+wrong),
                    (double)right/qstore.getCount()*100 , examobj.getLanguage());
            PerformanceDAO.addPerformance(perf);
            JOptionPane.showMessageDialog(null,"your performance has been saved");
            PaperSelectionFrame pf = new PaperSelectionFrame();
            pf.setVisible(true);
            this.dispose();
        }catch(SQLException e){
        JOptionPane.showMessageDialog(null, "error while saving");
        }
    }//GEN-LAST:event_btnDoneActionPerformed

