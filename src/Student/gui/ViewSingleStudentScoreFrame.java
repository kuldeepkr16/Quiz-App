    private void jcExamIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcExamIdActionPerformed
       if(examIdNotSelected){
           examIdNotSelected = false;
           return;
       }
       try{
        StudentScore ss = PerformanceDAO.getResult(jcStudentID.getSelectedItem().toString(),jcExamId.getSelectedItem().toString());
        txtLanguage.setText(ss.getLanguage());
       txtPercentage.setText(ss.getPercentage()+"");
       }catch(SQLException e){
           
       }
    }//GEN-LAST:event_jcExamIdActionPerformed

    private void jcStudentIDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcStudentIDActionPerformed
        if(studentIdNotSelected){
            studentIdNotSelected = false;
            return;
        }
       try{
           ArrayList<String> examIdList = PerformanceDAO.getAllExamID(jcStudentID.getSelectedItem().toString());
           for(String examid : examIdList){
               jcExamId.addItem(examid);
           }
       }catch(SQLException e){
           JOptionPane.showMessageDialog(null,"error in db","error",JOptionPane.ERROR_MESSAGE);
       }
    }//GEN-LAST:event_jcStudentIDActionPerformed

