    private void loadStudentId(){
        try{
            ArrayList<String> userid  = PerformanceDAO.getAllStudentId();
            if(userid.isEmpty()){
                JOptionPane.showMessageDialog(null,"No Students have yet given any exam","error",JOptionPane.ERROR_MESSAGE);
                return;
            }
            jcStudentID.removeAllItems();
            for(String id : userid){
                jcStudentID.addItem(id);
            }
        }catch(SQLException e){
            
        }
    }
    
